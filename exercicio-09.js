function checkNumber(number) {
    if (number > 0) {
        console.log("O número é positivo.");
    } else if (number < 0) {
        console.log("O número é negativo.");
    } else {
        console.log("O número é zero.")
    }
}

checkNumber(0);
checkNumber(12);
checkNumber(-10);